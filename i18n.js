let currentLang = localStorage.getItem("lang") || "ko";

function t(key, vars = {}) {
  const dict = TRANSLATIONS[currentLang] || TRANSLATIONS.ko;
  let text = dict[key] ?? TRANSLATIONS.ko[key] ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replaceAll(`{${k}}`, String(v));
  });
  return text;
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem("lang", lang);
  document.documentElement.lang = lang;
}

function getLanguage() {
  return currentLang;
}

function formatNumber(n) {
  const locale = currentLang === "ko" ? "ko-KR" : "en-US";
  return n.toLocaleString(locale);
}

function formatAttemptsUnit(count) {
  const unit = t("play.attemptsUnit");
  return unit ? `${count}${unit}` : String(count);
}
